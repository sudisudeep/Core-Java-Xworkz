class Subtraction{
	
	public static void main(String[] args){
		System.out.println(sub(12,23));
		System.out.println(sub(12,23));
		System.out.println(sub(12,23,74));
		System.out.println(sub(12,23,97));
		
	}
	
	public static int sub(int a,int b){
		return a-b;
	}
	
	public static int sub(int a,int b, int c){
		return a-b-c;
	}
	
	
	
}